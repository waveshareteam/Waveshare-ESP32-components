static esp_err_t panel_sd5207_disp_on_off(esp_lcd_panel_t *panel, bool on_off);

esp_err_t esp_lcd_new_panel_sd5207(const esp_lcd_panel_io_handle_t io, const esp_lcd_panel_dev_config_t *panel_dev_config,
                                   esp_lcd_panel_handle_t *ret_panel)
{
    ESP_LOGI(TAG, "version: %d.%d.%d", ESP_LCD_SD5207_VER_MAJOR, ESP_LCD_SD5207_VER_MINOR,
             ESP_LCD_SD5207_VER_PATCH);
    ESP_RETURN_ON_FALSE(io && panel_dev_config && ret_panel, ESP_ERR_INVALID_ARG, TAG, "invalid arguments");
    sd5207_vendor_config_t *vendor_config = (sd5207_vendor_config_t *)panel_dev_config->vendor_config;
    ESP_RETURN_ON_FALSE(vendor_config && vendor_config->mipi_config.dpi_config && vendor_config->mipi_config.dsi_bus, ESP_ERR_INVALID_ARG, TAG,
                        "invalid vendor config");

    esp_err_t ret = ESP_OK;
    sd5207_panel_t *sd5207 = (sd5207_panel_t *)calloc(1, sizeof(sd5207_panel_t));
    ESP_RETURN_ON_FALSE(sd5207, ESP_ERR_NO_MEM, TAG, "no mem for sd5207 panel");

    if (panel_dev_config->reset_gpio_num >= 0) {
        gpio_config_t io_conf = {
            .mode = GPIO_MODE_OUTPUT,
            .pin_bit_mask = 1ULL << panel_dev_config->reset_gpio_num,
        };
        ESP_GOTO_ON_ERROR(gpio_config(&io_conf), err, TAG, "configure GPIO for RST line failed");
    }

    switch (panel_dev_config->rgb_ele_order) {
    case LCD_RGB_ELEMENT_ORDER_RGB:
        sd5207->madctl_val = 0;
        break;
    case LCD_RGB_ELEMENT_ORDER_BGR:
        sd5207->madctl_val |= LCD_CMD_BGR_BIT;
        break;
    default:
        ESP_GOTO_ON_FALSE(false, ESP_ERR_NOT_SUPPORTED, err, TAG, "unsupported color space");
        break;
    }

    sd5207->io = io;
    sd5207->init_cmds = vendor_config->init_cmds;
    sd5207->init_cmds_size = vendor_config->init_cmds_size;
    sd5207->reset_gpio_num = panel_dev_config->reset_gpio_num;
    sd5207->flags.reset_level = panel_dev_config->flags.reset_active_high;

    // Create MIPI DPI panel
    esp_lcd_panel_handle_t panel_handle = NULL;
    ESP_GOTO_ON_ERROR(esp_lcd_new_panel_dpi(vendor_config->mipi_config.dsi_bus, vendor_config->mipi_config.dpi_config, &panel_handle), err, TAG,
                      "create MIPI DPI panel failed");
    ESP_LOGD(TAG, "new MIPI DPI panel @%p", panel_handle);

    // Save the original functions of MIPI DPI panel
    sd5207->del = panel_handle->del;
    sd5207->init = panel_handle->init;
    // Overwrite the functions of MIPI DPI panel
    panel_handle->del = panel_sd5207_del;
    panel_handle->init = panel_sd5207_init;
    panel_handle->reset = panel_sd5207_reset;
    panel_handle->mirror = panel_sd5207_mirror;
    panel_handle->invert_color = panel_sd5207_invert_color;
    panel_handle->disp_on_off = panel_sd5207_disp_on_off;
    panel_handle->user_data = sd5207;
    *ret_panel = panel_handle;
    ESP_LOGD(TAG, "new sd5207 panel @%p", sd5207);

    return ESP_OK;

err:
    if (sd5207) {
        if (panel_dev_config->reset_gpio_num >= 0) {
            gpio_reset_pin(panel_dev_config->reset_gpio_num);
        }
        free(sd5207);
    }
    return ret;
}

