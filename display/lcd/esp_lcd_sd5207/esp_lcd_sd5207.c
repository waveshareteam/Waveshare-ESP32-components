#define SD5207_CMD_SHLR_BIT       (1 << 0)
#define SD5207_CMD_UPDN_BIT       (1 << 1)

typedef struct {
    esp_lcd_panel_io_handle_t io;
    int reset_gpio_num;
    uint8_t madctl_val; // save current value of LCD_CMD_MADCTL register
    uint8_t colmod_val; // save surrent value of LCD_CMD_COLMOD register
    const sd5207_lcd_init_cmd_t *init_cmds;
    uint16_t init_cmds_size;
    struct {
        unsigned int reset_level: 1;
    } flags;
    // To save the original functions of MIPI DPI panel
    esp_err_t (*del)(esp_lcd_panel_t *panel);
    esp_err_t (*init)(esp_lcd_panel_t *panel);
} sd5207_panel_t;

static const char *TAG = "sd5207";

static esp_err_t panel_sd5207_del(esp_lcd_panel_t *panel);
static esp_err_t panel_sd5207_init(esp_lcd_panel_t *panel);
static esp_err_t panel_sd5207_reset(esp_lcd_panel_t *panel);
static esp_err_t panel_sd5207_invert_color(esp_lcd_panel_t *panel, bool invert_color_data);
