static esp_err_t panel_sd5207_del(esp_lcd_panel_t *panel)
{
    sd5207_panel_t *sd5207 = (sd5207_panel_t *)panel->user_data;

    // Delete MIPI DPI panel
    ESP_RETURN_ON_ERROR(sd5207->del(panel), TAG, "del sd5207 panel failed");
    if (sd5207->reset_gpio_num >= 0) {
        gpio_reset_pin(sd5207->reset_gpio_num);
    }
    ESP_LOGD(TAG, "del sd5207 panel @%p", sd5207);
    free(sd5207);

    return ESP_OK;
}

